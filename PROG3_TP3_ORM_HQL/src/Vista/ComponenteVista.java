    private void configTabla() {
        listaComponentes = new DefaultTableModel();
        listaComponentes.addColumn("Id");
        listaComponentes.addColumn("Nombre");
        listaComponentes.addColumn("N° Serie");
        listaComponentes.addColumn("Id Computadora");
    }
    
    public void actualizarTabla() {
        GestorComponente gc = new GestorComponente();
        List<Componente> componentes = gc.dameListaComponentes();
        GestorTabla.rellena(componentes, listaComponentes);
        gc.cierraConexion();
    }
    
    public void llenarComboBox(){ // Lleno el comboBox.
        GestorComponente gc = new GestorComponente();
        List<String> idPC = gc.dameIdComputadoras();
        Iterator<String> idPCiterator = idPC.iterator();
        while(idPCiterator.hasNext()){
           cboxComputadoras.addItem(idPCiterator.next());
        }
    }

