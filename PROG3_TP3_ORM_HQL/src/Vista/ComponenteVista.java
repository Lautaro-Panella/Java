    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        GestorComponente gc = new GestorComponente();
        int filaSeleccionada = tabla_componente.getSelectedRow();
        try {
            if (filaSeleccionada == -1) {
                if ("".equals(txtNombre.getText()) || "".equals(txtNroSerie.getText())) {
                    javax.swing.JOptionPane.showMessageDialog(this, "Los campos Nombre y N° Serie son obligatorios.", "Error:", javax.swing.JOptionPane.INFORMATION_MESSAGE);
                } else {
                gc.insertarComponente(txtNombre.getText(), txtNroSerie.getText(), Long.parseLong(cboxComputadoras.getSelectedItem().toString()));
                actualizarTabla();
                javax.swing.JOptionPane.showMessageDialog(this, "Componente guardado!", "Acción:", javax.swing.JOptionPane.INFORMATION_MESSAGE);
                }
            }
            else {
                Long valorId = (Long) listaComponentes.getValueAt(filaSeleccionada, 0);
                gc.modificarComponente(valorId, txtNombre.getText(), txtNroSerie.getText(), Long.parseLong(cboxComputadoras.getSelectedItem().toString()));
                actualizarTabla();
                javax.swing.JOptionPane.showMessageDialog(this, "Componente modificado!", "Acción:", javax.swing.JOptionPane.INFORMATION_MESSAGE);
            }    
        } catch(Exception ex) {
            Logger.getLogger(ComponenteVista.class.getName()).log(Level.SEVERE, null, ex);
        }   
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        GestorComponente gc = new GestorComponente();
        int filaSeleccionada = tabla_componente.getSelectedRow();
        try {
            if (filaSeleccionada == -1) {
                javax.swing.JOptionPane.showMessageDialog(this, "Seleccione un componente de la lista.", "Error:", javax.swing.JOptionPane.INFORMATION_MESSAGE);
            }
            else {
                Long valorId = (Long) listaComponentes.getValueAt(filaSeleccionada, 0);
                txtNombre.setText(gc.dameComponente(valorId).getNombre());
                txtNroSerie.setText(gc.dameComponente(valorId).getNroSerie());
            }
        } catch(Exception ex) {
            Logger.getLogger(ComponenteVista.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnModificarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int filaSeleccionada = tabla_componente.getSelectedRow();       
        try {
            if (filaSeleccionada == -1) {
                javax.swing.JOptionPane.showMessageDialog(this, "Seleccione un componente de la lista.", "Error:", javax.swing.JOptionPane.INFORMATION_MESSAGE);
            }
            else {
                Long valorId = (Long) listaComponentes.getValueAt(filaSeleccionada, 0);
                GestorComponente gc = new GestorComponente();
                gc.eliminarComponente(valorId);
                actualizarTabla();
                javax.swing.JOptionPane.showMessageDialog(this, "Componente eliminado!", "Acción:", javax.swing.JOptionPane.INFORMATION_MESSAGE);
            }
        } catch(Exception ex) {
            Logger.getLogger(ComponenteVista.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

